####################################################
# 17.3 Few-shot: 대표·경계 예시를 함께 제공
####################################################
examples = [
    ("택배가 안 왔어요", "배송"),
    ("결제 취소하고 싶어요", "환불"),
    ("배송도 늦고 환불도 원해요", "환불"),
]
query = "상품이 안 왔고 결제를 취소하고 싶어요"

print("=== Few-shot 예시 ===")
for text, label in examples:
    print(f"{text} -> {label}")
print("새 입력:", query)
print("예시의 경계 규칙에 따른 예상:", "환불")
