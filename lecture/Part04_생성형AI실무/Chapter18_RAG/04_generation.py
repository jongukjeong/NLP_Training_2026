question = "환불 처리 기간은?"
context = "환불은 접수 후 영업일 기준 3일 이내 처리됩니다."

if context and "환불" in context:
    answer = "환불은 접수 후 영업일 기준 3일 이내 처리됩니다. [문서 1]"
else:
    answer = "제공된 문서에서 답을 찾지 못했습니다."

print("질문:", question)
print("근거:", context)
print("답변:", answer)
