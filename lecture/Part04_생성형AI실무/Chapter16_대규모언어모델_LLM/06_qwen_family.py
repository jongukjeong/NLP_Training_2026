scores = {
    "한국어 질의응답": 0.82,
    "영어 요약": 0.88,
    "코드 생성": 0.79,
    "형식 준수": 0.91,
}

print("=== 업무 평가 결과 ===")
for task, score in scores.items():
    print(f"{task:12s}: {score:.0%}")
print("평균:", f"{sum(scores.values()) / len(scores):.1%}")
