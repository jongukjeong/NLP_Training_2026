# 10.1 Seq2Seq: 가변 길이 입력을 가변 길이 출력으로 변환
####################################################
source = ["나는", "학생", "입니다"]
target = ["I", "am", "a", "student"]

print("=== Seq2Seq 입·출력 ===")
print("입력 토큰:", source, "길이:", len(source))
print("출력 토큰:", target, "길이:", len(target))
print("입력과 출력의 길이가 같을 필요는 없습니다.")
